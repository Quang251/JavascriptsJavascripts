/*
Math object
- Math.PI
- Math.round()
- Math.abs()
- Math.ceil()
- Math.floor()
- Math.random()
- Math.max()
- Math.min()
- Math.pow()
- Math.sqrt()
- Math.sin()
- Math.cos()
*/

//Math.PI
console.log(Math.PI);

//Math.round()
console.log(Math.round(1.3)); //làm tròn số 1.3 thành 1  

//Math.abs()
console.log(Math.abs(-3)); //lấy giá trị tuyệt đối của số -3 thành 3

//Math.ceil()
console.log(Math.ceil(3.1)); //làm tròn số 3.1 thành 4 (tròn trên)

//Math.floor()
console.log(Math.floor(3.9)); //làm tròn số 3.9 thành 3 (tròn dưới)

//Math.random()
console.log(Math.random()); //lấy số ngẫu nhiên từ 0 đến 1
console.log(Math.floor(Math.random() * 10)); //lấy số ngẫu nhiên từ 0 đến 9
 
//Math.max()
console.log(Math.max(1, 2, 3, 4, 5)); //lấy số lớn nhất trong các số 1, 2, 3, 4, 5

//Math.min()
console.log(Math.min(1, 2, 3, 4, 5)); //lấy số nhỏ nhất trong các số 1, 2, 3, 4, 5

//Math.pow()
console.log(Math.pow(2, 3)); //lấy số 2 mũ 3 thành 8 

//Math.sqrt()
console.log(Math.sqrt(16)); //lấy căn bậc 2 của số 16 thành 4

//Math.sin()
console.log(Math.sin(Math.PI / 2)); //lấy sin của số PI / 2 thành 1

//Math.cos()
console.log(Math.cos(Math.PI / 2)); //lấy cos của số PI / 2 thành 0

//Math.tan()
console.log(Math.tan(Math.PI / 4)); //lấy tan của số PI / 4 thành 1 